//Write a program to count down from 10 to 0, printing each number. 


class CountDown{
    public static void main(String args[]){
		int num = 10;
        
		System.out.println("The count down from 10 to 0 are :");
        while (num >= 0) {
            System.out.println(num);     
            num--;
        }
	    
	}
}	
 
	
